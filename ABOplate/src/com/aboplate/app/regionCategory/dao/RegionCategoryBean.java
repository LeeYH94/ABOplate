public class RegionCategoryBean {
	private String region_category_city;
	private String region_category_town;
	private String region_category_township;
	
	public RegionCategoryBean() {;}

	public String getRegion_category_city() {
		return region_category_city;
	}

	public void setRegion_category_city(String region_category_city) {
		this.region_category_city = region_category_city;
	}

	public String getRegion_category_town() {
		return region_category_town;
	}

	public void setRegion_category_town(String region_category_town) {
		this.region_category_town = region_category_town;
	}

	public String getRegion_category_township() {
		return region_category_township;
	}

	public void setRegion_category_township(String region_category_township) {
		this.region_category_township = region_category_township;
	}

	
}
